def cleanup_imagefile():
    try:
        imgfile = opts.outFile
    except:
        return
    try:
        os.remove(imgfile)
    except:
        print(c.yellow("[Cleanup] Deleting image file '{}' failed; you can safely delete it yourself".format(imgfile)))
    else:
        c.verbose("[Cleanup] Deleted image file '{}'".format(imgfile))
